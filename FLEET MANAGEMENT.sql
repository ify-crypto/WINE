CREATE TABLE VEHICLES(
VEHICLE_ID VARCHAR (10) PRIMARY KEY NOT NULL,
LICENSE_PLATE VARCHAR(10) NOT NULL,
MAKE VARCHAR(10) NOT NULL,
MODEL VARCHAR(10) NOT NULL,
 YEAR  INT  NOT NULL,
 COLOR VARCHAR(10) NOT NULL,
 VIN VARCHAR(20) NOT NULL
 );


 CREATE TABLE DRIVERS( 
 DRIVERS_ID VARCHAR (5)  PRIMARY KEY NOT NULL,
 FIRST_NAME  VARCHAR(10) NOT NULL,
 LAST_NAME VARCHAR (10) NOT NULL,
 LICENSE_NUMBER VARCHAR (10) NOT NULL,
 PHONE INT NOT NULL,
 ADDRESS VARCHAR(14) NOT NULL,
 CITY VARCHAR(10) NOT NULL,
 STATE VARCHAR(5) NOT NULL,
 ZIPCODE INT NOT NULL
 );


 


 CREATE TABLE TRIPS(
 TRIPS_ID VARCHAR(8) PRIMARY KEY NOT NULL,
 START_DATE DATE NOT NULL,
 END_DATE DATE NOT NULL,
 START_LOCATION VARCHAR(18) NOT NULL,
 END_LOCATION VARCHAR(18) NOT NULL,
 DISTANCE INT NOT NULL,
 VEHICLE_ID VARCHAR(10)  FOREIGN KEY REFERENCES VEHICLES(VEHICLE_ID) NOT NULL,
 DRIVERS_ID VARCHAR(5) FOREIGN KEY REFERENCES DRIVERS(DRIVERS_ID) NOT NULL 
 );


 CREATE TABLE MAINTENANCE(
 MAINTENANCE_ID VARCHAR(10) PRIMARY KEY NOT NULL,
 MAINTENANCE_DATE DATE NOT NULL,
 DESCRIPTION VARCHAR(20) NOT NULL,
 COST FLOAT NOT NULL,
 VEHICLE_ID VARCHAR(10)FOREIGN KEY REFERENCES VEHICLES(VEHICLE_ID)
 );



